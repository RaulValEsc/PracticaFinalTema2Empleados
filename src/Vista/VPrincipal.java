    public void hacerRegistro() {
        int i = controladorBD.fichar(etDNI.getText());
        if (i == 1) {
            etiqueta.setVisible(true);
            etiqueta.setText("DNI INCORRECTO");
            etiqueta.setForeground(Color.red);
            
        } else if (i == 2) {
            etiqueta.setVisible(true);
            etiqueta.setText("ADIOS, QUE TENGAS BUEN DIA");
            etiqueta.setForeground(Color.blue);
            
        } else if (i == 3) {
            etiqueta.setVisible(true);
            etiqueta.setText("BIENVENIDO AL TRABAJO");
            etiqueta.setForeground(Color.magenta);

        }
        Timer timer = new Timer();  
                TimerTask tarea = new TimerTask() {  
                    @Override  
                    public void run() {  
  
                        etiqueta.setText("");
                        etDNI.setText("");
                    }  
                };  
                timer.schedule(tarea, 1500);

    }

